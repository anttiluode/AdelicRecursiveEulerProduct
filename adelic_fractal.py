import numpy as np
import heapq
import time
from typing import Optional

# ─────────────────────────────────────────────
# 1.  Utility: sieve for first N primes
# ─────────────────────────────────────────────
def get_primes(n: int) -> np.ndarray:
    out, k = [], 2
    while len(out) < n:
        if all(k % d for d in range(2, int(k**0.5) + 1)):
            out.append(k)
        k += 1
    return np.array(out, dtype=float)


# ─────────────────────────────────────────────
# 2.  Euler lattice: multiplicative composite
#     frequencies generated by a min-heap
# ─────────────────────────────────────────────
def build_euler_lattice(seeds: np.ndarray,
                        max_composites: int = 1500) -> tuple[np.ndarray, np.ndarray]:
    """
    Given seeds s_1 … s_K (positive reals), treat them as "primes" and
    build the multiplicative lattice:

        F = Σ_k  c_k · log(s_k)     c_k ∈ ℤ_≥0,  not all zero

    ordered by F.  Amplitude A(F) = exp(-F/2)  ⟹  A(F_n) = 1/√(∏ s_k^{c_k}).

    Returns (freqs, amps) with the zero-frequency "1" excluded.
    """
    base_freqs = np.log(seeds)
    heap = [(0.0, 0)]          # (frequency, max_base_index)
    seen = {round(0.0, 7)}
    composites: list[float] = []

    while len(composites) < max_composites and heap:
        freq, max_idx = heapq.heappop(heap)
        composites.append(freq)
        for i in range(max_idx, len(base_freqs)):
            nf = freq + base_freqs[i]
            q  = round(nf, 7)
            if q not in seen:
                seen.add(q)
                heapq.heappush(heap, (nf, i))

    freqs = np.array(composites[1:])          # drop F=0
    amps  = np.exp(-freqs / 2.0)
    return freqs, amps


# ─────────────────────────────────────────────
# 3.  Build complex manifold Z(t)
# ─────────────────────────────────────────────
def build_manifold(freqs: np.ndarray,
                   amps:  np.ndarray,
                   t_arr: np.ndarray) -> np.ndarray:
    """Z(t) = Σ_k A_k exp(i F_k t)   vectorised over t."""
    z = np.zeros(len(t_arr), dtype=np.complex128)
    for f, a in zip(freqs, amps):
        z += a * np.exp(1j * f * t_arr)
    return z


# ─────────────────────────────────────────────
# 4.  Find scars (deep destructive-interference
#     minima of |Z(t)|)
# ─────────────────────────────────────────────
def find_scars(z: np.ndarray,
               t_arr: np.ndarray,
               threshold: float = 0.70,
               smooth_window: int = 50) -> np.ndarray:
    """
    Returns scar positions (continuous t values via parabolic interpolation).
    threshold : scar must be < threshold × local_mean
    """
    mag = np.abs(z)
    # local minima
    min_idx = np.where((mag[1:-1] < mag[:-2]) & (mag[1:-1] < mag[2:]))[0] + 1
    # dynamic threshold
    local_mean = np.convolve(mag, np.ones(smooth_window) / smooth_window, mode='same')
    deep = min_idx[mag[min_idx] < threshold * local_mean[min_idx]]

    dt = t_arr[1] - t_arr[0]
    scars = []
    for idx in deep:
        a, b, c = mag[idx - 1], mag[idx], mag[idx + 1]
        denom = a - 2 * b + c
        if abs(denom) > 1e-10:
            shift = 0.5 * (a - c) / denom
            scars.append(t_arr[idx] + shift * dt)
        else:
            scars.append(t_arr[idx])
    return np.array(scars)


# ─────────────────────────────────────────────
# 5.  Unfolded spacing variance
# ─────────────────────────────────────────────
def unfolded_variance(scars: np.ndarray, window: int = 20) -> Optional[float]:
    """
    Unfold scar spacings against a local rolling mean, then return Var(s).
    GUE theoretical value ≈ 0.136
    GOE theoretical value ≈ 0.286
    Poisson               = 1.000
    """
    if len(scars) < 15:
        return None
    spacings = np.diff(scars)
    w = min(window, len(spacings) // 2)
    unfolded = []
    for i in range(len(spacings)):
        lo, hi = max(0, i - w), min(len(spacings), i + w)
        lm = np.mean(spacings[lo:hi])
        if lm > 0:
            unfolded.append(spacings[i] / lm)
    return float(np.var(unfolded)) if unfolded else None


# ─────────────────────────────────────────────
# 6.  One full simulation run (all levels)
# ─────────────────────────────────────────────
def run_simulation(n_primes:    int   = 40,
                   n_composites: int  = 1200,
                   n_scar_seeds: int  = 60,
                   n_levels:     int  = 4,
                   t_min: float  = 10.0,
                   t_max: float  = 1000.0,
                   dt:    float  = 0.05,
                   verbose: bool = True) -> list[dict]:
    """
    Run the recursive Euler construction for n_levels.
    Returns a list of per-level dictionaries.
    """
    t_arr = np.arange(t_min, t_max, dt)
    seeds = get_primes(n_primes)
    results = []

    labels = {
        v: name
        for v, name in [
            (lambda v: v < 0.20,  "GOE-adjacent"),
            (lambda v: 0.20 <= v < 0.35, "GOE"),
            (lambda v: 0.11 <= v < 0.18, "GUE ✓"),
        ]
    }

    def classify(v):
        if v is None:     return "too few scars"
        if v > 0.80:      return "Poisson (random)"
        if 0.24 < v < 0.35: return "GOE"
        if 0.11 < v < 0.18: return "GUE ✓"
        return "intermediate"

    for level in range(n_levels):
        t0 = time.time()
        freqs, amps = build_euler_lattice(seeds, n_composites)
        z     = build_manifold(freqs, amps, t_arr)
        scars = find_scars(z, t_arr)
        var   = unfolded_variance(scars)
        elapsed = time.time() - t0

        rec = dict(level=level, n_seeds=len(seeds),
                   n_lattice=len(freqs), n_scars=len(scars),
                   variance=var, elapsed=elapsed,
                   freqs=freqs, amps=amps, scars=scars)
        results.append(rec)

        if verbose:
            vstr = f"{var:.4f} → {classify(var)}" if var else "—"
            print(f"Level {level}  seeds={len(seeds):4d}  "
                  f"lattice={len(freqs):5d}  scars={len(scars):4d}  "
                  f"σ²={vstr}  ({elapsed:.2f}s)")

        # next level's seeds = top scars
        if len(scars) > 5:
            seeds = scars[:n_scar_seeds]
        else:
            if verbose:
                print("  ⚠ Lineage died — too few scars.")
            break

    return results


